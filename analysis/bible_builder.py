_SCENE_BIBLE_SYSTEM = """\
You are a dramaturg analyzing a single scene. Given the utterances and beat states,
produce a SCENE BIBLE describing the dramatic engine of this scene.

Return ONLY valid JSON:
{
  "dramatic_pressure": "the overall pressure or urgency driving this scene",
  "what_changes": "what concretely changes by the end of this scene",
  "hidden_tensions": "subterranean conflicts not spoken aloud",
  "beat_map": "prose summary of how the beats progress through this scene"
}
"""

_SCENE_BIBLE_USER = """\
PLAY: {play_title} | ACT {act} SCENE {scene}

UTTERANCES:
{utterance_block}

BEAT STATES:
{beat_states_block}

Produce the SCENE BIBLE as JSON.
"""


def build_scene_bible(act_num: int, scene_num: int, play: Play) -> SceneBible:
    """Build a SceneBible for one scene."""
    scene_obj = next(
        (s for a in play.acts if a.number == act_num for s in a.scenes if s.scene == scene_num),
        None,
    )
    if scene_obj is None:
        return SceneBible(play_id=play.id, act=act_num, scene=scene_num)

    utterances = [u for b in scene_obj.beats for u in b.utterances]
    utt_block = "\n".join(f"  {u.speaker}: {u.text}" for u in utterances[:60])

    beat_states_lines = []
    for beat in scene_obj.beats:
        for bs in beat.beat_states:
            beat_states_lines.append(
                f"  Beat {beat.index} | {bs.character}: tactic={bs.tactic_state!r}"
                f" want={bs.desire_state!r}"
            )
    bs_block = "\n".join(beat_states_lines)

    response = client.messages.create(
        model=get_model("bible"),
        max_tokens=1024,
        system=_SCENE_BIBLE_SYSTEM,
        messages=[{"role": "user", "content": _SCENE_BIBLE_USER.format(
            play_title=play.title,
            act=act_num,
            scene=scene_num,
            utterance_block=utt_block,
            beat_states_block=bs_block,
        )}],
    )
    raw = response.content[0].text.strip()
    if raw.startswith("```"):
        raw = raw.split("```")[1].lstrip("json").strip().rstrip("```").strip()
    try:
        data = json.loads(raw)
    except json.JSONDecodeError:
        data = {}

    bible = SceneBible(
        play_id=play.id,
        act=act_num,
        scene=scene_num,
        dramatic_pressure=data.get("dramatic_pressure", ""),
        what_changes=data.get("what_changes", ""),
        hidden_tensions=data.get("hidden_tensions", ""),
        beat_map=data.get("beat_map", ""),
    )
    return bible


