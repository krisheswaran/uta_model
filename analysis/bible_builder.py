"""
Bible Builder — Analysis Pipeline Step 5

Aggregates BeatStates into the three output artefacts:
  - CharacterBible (per character)
  - SceneBible (per scene)
  - WorldBible (per play)

Each bible is generated by an LLM reading the full set of BeatStates
and producing a structured synthesis. The CharacterBible also includes
a tactic distribution (counted from the BeatStates) and few-shot lines
sampled from canonical utterances.
"""
from __future__ import annotations

import json
import logging
import sys
from collections import Counter
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent.parent))

import anthropic
from config import ANTHROPIC_API_KEY, BIBLES_DIR, get_model
from schemas import (
    Beat, BeatState, CharacterBible, Play, SceneBible, WorldBible,
)

client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)
logger = logging.getLogger(__name__)

# ────────────────────────────────────────────────────────────────────────────
# Character Bible
# ────────────────────────────────────────────────────────────────────────────

_CHARACTER_BIBLE_SYSTEM = """\
You are a dramaturg and acting coach synthesizing a comprehensive character analysis.
Given a character's BeatState arc across a play, produce a structured CHARACTER BIBLE.

Return ONLY valid JSON with this structure:
{
  "superobjective": "the character's deepest, play-long want",
  "wounds_fears_needs": "enduring psychological wounds, fears, and unmet needs",
  "recurring_tactics": ["tactic1", "tactic2", ...],
  "preferred_defense_mechanisms": ["defense1", "defense2", ...],
  "psychological_contradictions": ["contradiction1", "contradiction2", ...],
  "speech_style": "description of how they speak (rhythm, vocabulary, rhetorical habits)",
  "rhetorical_patterns": ["pattern1", "pattern2", ...],
  "arc_by_scene": {"act_scene": "what changes in this scene for the character", ...},
  "known_facts": ["fact1", "fact2"],
  "secrets": ["secret1", "secret2"]
}
"""

_CHARACTER_BIBLE_USER = """\
PLAY: {play_title}
CHARACTER: {character}

BEAT-BY-BEAT ARC:
{arc_block}

SAMPLE CANONICAL LINES:
{sample_lines}

Synthesize the CHARACTER BIBLE as JSON.
"""


def _collect_character_arc(character: str, play: Play) -> tuple[list[BeatState], list[str]]:
    """Return (beat_states, sample_lines) for a character across the whole play."""
    beat_states: list[BeatState] = []
    sample_lines: list[str] = []
    for act in play.acts:
        for scene in act.scenes:
            for beat in scene.beats:
                for bs in beat.beat_states:
                    if bs.character == character:
                        beat_states.append(bs)
                # Collect a few lines from this character
                for utt in (u for b in scene.beats for u in b.utterances):
                    if utt.speaker == character and len(utt.text) > 20:
                        sample_lines.append(utt.text)
    # Sample up to 20 lines evenly across the play
    if len(sample_lines) > 20:
        step = len(sample_lines) // 20
        sample_lines = sample_lines[::step][:20]
    return beat_states, sample_lines


def _arc_to_str(beat_states: list[BeatState]) -> str:
    lines = []
    for bs in beat_states:
        lines.append(
            f"{bs.beat_id}: want={bs.desire_state!r} | tactic={bs.tactic_state!r} | "
            f"affect(v={bs.affect_state.valence:.1f},a={bs.affect_state.arousal:.1f}) | "
            f"defense={bs.defense_state!r} | contradiction={bs.psychological_contradiction!r}"
        )
    return "\n".join(lines)


_MAX_ARC_ENTRIES = 30


def _sample_beat_states(beat_states: list[BeatState], max_entries: int = _MAX_ARC_ENTRIES) -> tuple[list[BeatState], bool]:
    """Return (sampled_beat_states, was_sampled).

    Always preserves the first and last entries so arc endpoints are intact.
    Samples evenly across the middle when len > max_entries.
    """
    if len(beat_states) <= max_entries:
        return beat_states, False
    # Always keep first and last; sample the middle
    middle = beat_states[1:-1]
    step = max(1, len(middle) // (max_entries - 2))
    sampled_middle = middle[::step][: max_entries - 2]
    return [beat_states[0]] + sampled_middle + [beat_states[-1]], True


def _parse_json_response(raw: str) -> dict:
    """Strip markdown fences and parse JSON from LLM response."""
    text = raw.strip()
    if text.startswith("```"):
        text = text.split("```")[1].lstrip("json").strip().rstrip("```").strip()
    return json.loads(text)


def build_character_bible(character: str, play: Play) -> CharacterBible:
    """Build a CharacterBible for one character using LLM synthesis."""
    beat_states, sample_lines = _collect_character_arc(character, play)
    if not beat_states:
        return CharacterBible(play_id=play.id, character=character)

    tactic_counts = Counter(bs.tactic_state for bs in beat_states if bs.tactic_state)

    # Sample arc for large characters to avoid overwhelming the LLM
    arc_for_prompt, was_sampled = _sample_beat_states(beat_states)
    arc_block = _arc_to_str(arc_for_prompt)
    if was_sampled:
        arc_block = (
            f"[Note: {len(arc_for_prompt)} of {len(beat_states)} beat_states shown, "
            f"sampled evenly across the arc. First and last are included.]\n" + arc_block
        )

    prompt = _CHARACTER_BIBLE_USER.format(
        play_title=play.title,
        character=character,
        arc_block=arc_block,
        sample_lines="\n".join(f"  - {l}" for l in sample_lines),
    )

    data = _call_and_parse_character_bible(character, prompt, len(beat_states))

    bible = CharacterBible(
        play_id=play.id,
        character=character,
        superobjective=data.get("superobjective", ""),
        wounds_fears_needs=data.get("wounds_fears_needs", ""),
        recurring_tactics=data.get("recurring_tactics", []),
        preferred_defense_mechanisms=data.get("preferred_defense_mechanisms", []),
        psychological_contradictions=data.get("psychological_contradictions", []),
        speech_style=data.get("speech_style", ""),
        rhetorical_patterns=data.get("rhetorical_patterns", []),
        few_shot_lines=sample_lines[:10],
        known_facts=data.get("known_facts", []),
        secrets=data.get("secrets", []),
        arc_by_scene=data.get("arc_by_scene", {}),
        tactic_distribution=dict(tactic_counts),
    )
    return bible


def _call_and_parse_character_bible(character: str, prompt: str, beat_state_count: int) -> dict:
    """Call the LLM and parse JSON, with one retry on failure."""
    response = client.messages.create(
        model=get_model("bible"),
        max_tokens=4096,
        system=_CHARACTER_BIBLE_SYSTEM,
        messages=[{"role": "user", "content": prompt}],
    )
    raw = response.content[0].text

    try:
        return _parse_json_response(raw)
    except json.JSONDecodeError:
        logger.warning(
            "JSON parse failed for character %s (%d beat_states). "
            "Raw response starts with: %.200s",
            character, beat_state_count, raw,
        )

    # Retry once with an explicit JSON-only reminder
    logger.info("Retrying character bible for %s with JSON reminder...", character)
    retry_prompt = (
        prompt + "\n\nIMPORTANT: Your previous response was not valid JSON. "
        "Return ONLY a single JSON object with no commentary, no markdown fences, "
        "and no text before or after the JSON."
    )
    response = client.messages.create(
        model=get_model("bible"),
        max_tokens=4096,
        system=_CHARACTER_BIBLE_SYSTEM,
        messages=[{"role": "user", "content": retry_prompt}],
    )
    raw = response.content[0].text

    try:
        return _parse_json_response(raw)
    except json.JSONDecodeError:
        logger.error(
            "JSON parse failed AGAIN for character %s on retry. "
            "Raw response starts with: %.200s",
            character, raw,
        )
        return {}


